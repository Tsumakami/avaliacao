class InscricaoForm {
  constructor(nome, email, disponibilidade, interesses, mensagem) {
    this.nome;
    this.email;
    this.disponibilidade;
    this.interesses;
    this.mensagem;
  }
  get nome(){
    return this.nome;
  }
  get email(){
    return this.email;
  }
  get disponibilidade(){
    return this.disponibilidade;
  }
  get interesses(){
    return this.interesses;
  }
  get mensagem(){
    return this.mensagem;
  }

}
